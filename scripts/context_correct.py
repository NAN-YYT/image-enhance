def correct_text(text):
    """Apply domain-specific corrections to OCR output."""
    corrected = text
    for wrong, right in sorted(CORRECTIONS.items(), key=lambda x: -len(x[0])):
        corrected = corrected.replace(wrong, right)
    return corrected


def process_results(results_path):
    """Load OCR results and apply corrections."""
    with open(results_path, "r", encoding="utf-8") as f:
        results = json.load(f)

    print("=" * 60)
    print("CORRECTED OCR OUTPUT")
    print("=" * 60)

    for region in results:
        print(f"\n--- Region {region['region']} ---")
        for line in region["content"]:
            original = line["text"]
            corrected = correct_text(original)
            marker = "✓" if line["confidence"] > 0.7 else "~" if line["confidence"] >= 0.4 else "?"
            if corrected != original:
                print(f"  {marker} {corrected}")
                print(f"    [raw: {original}]")
            else:
                print(f"  {marker} {corrected}")


if __name__ == "__main__":
    if len(sys.argv) < 2:
        print("Usage: python3 context_correct.py <results.json>")
        sys.exit(1)
    process_results(sys.argv[1])
